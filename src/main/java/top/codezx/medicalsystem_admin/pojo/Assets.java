package top.codezx.medicalsystem_admin.pojo;



public class Assets {

  private Integer assets;
  private String personInCharge;
  private String affiliatedUnits;
  private Integer apacity;
  private Integer roomId;
  private Integer floorId;
  private Integer buildingId;


  public Integer getAssets() {
    return assets;
  }

  public void setAssets(Integer assets) {
    this.assets = assets;
  }


  public String getPersonInCharge() {
    return personInCharge;
  }

  public void setPersonInCharge(String personInCharge) {
    this.personInCharge = personInCharge;
  }


  public String getAffiliatedUnits() {
    return affiliatedUnits;
  }

  public void setAffiliatedUnits(String affiliatedUnits) {
    this.affiliatedUnits = affiliatedUnits;
  }


  public Integer getApacity() {
    return apacity;
  }

  public void setApacity(Integer apacity) {
    this.apacity = apacity;
  }


  public Integer getRoomId() {
    return roomId;
  }

  public void setRoomId(Integer roomId) {
    this.roomId = roomId;
  }


  public Integer getFloorId() {
    return floorId;
  }

  public void setFloorId(Integer floorId) {
    this.floorId = floorId;
  }


  public Integer getBuildingId() {
    return buildingId;
  }

  public void setBuildingId(Integer buildingId) {
    this.buildingId = buildingId;
  }

}
